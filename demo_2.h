//容器嵌套容器
#include <iostream>
using namespace std;
#include<vector>
#include <string>
#include <algorithm>

void test_4()
{
    vector<vector<int>>v;
    //创建小容器
    vector<int>v1;
    vector<int>v2;
    vector<int>v3;
    vector<int>v4;
    vector<int>v5;
    //向小容器中添加数据
    for(int i=0;i<4;i++)
    {
        v1.push_back(i+1);
        v2.push_back(i+2);
        v3.push_back(i+3);
        v4.push_back(i+4);
        v5.push_back(i+5);
    }
    //将小容器插入到大容器中
    v.push_back(v1);
    v.push_back(v2);
    v.push_back(v3);
    v.push_back(v4);
    v.push_back(v5);
    //通过大容器吧所有数据遍历一遍
    for(auto i:v)
    {
        for(auto j:i)
        {
            cout<<j<<"  ";
        }
        cout<<endl;
    }
}